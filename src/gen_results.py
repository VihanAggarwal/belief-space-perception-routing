"""Scan all committed track outputs and emit a single consolidated RESULTS.md.

Aggregates per-track: data summary, RQ-H (joint vs decoupled), RQ-A1, learned-router
baseline, phase-diagram sweep, and the extended 9-config frontier. Run:
    python src/gen_results.py
"""
from __future__ import annotations

import glob
import json
from pathlib import Path

import pandas as pd

ROOT = Path(__file__).resolve().parent.parent
OUT = ROOT / "outputs"

NAMES = {
    "trackA": "Track A - TartanDrive off-road (full 1198 frames)",
    "trackA_309": "Track A - TartanDrive off-road (309-frame segment)",
    "trackC": "Track C - WoodScape Soiling, fisheye (lens contamination)",
    "trackD_rain_4_0": "Track D - RADIATE rain (real adverse weather)",
    "trackD_snow_1_0": "Track D - RADIATE snow",
    "trackD_fog_6_0": "Track D - RADIATE fog",
    "trackD_night_1_0": "Track D - RADIATE night",
}
ORDER = ["trackD_fog_6_0", "trackD_night_1_0", "trackD_rain_4_0", "trackD_snow_1_0",
         "trackC", "trackA", "trackA_309"]


def jload(p):
    try:
        return json.load(open(p))
    except Exception:
        return None


def deadline_ms(t):
    s = jload(OUT / t / "phase3" / "phase3_summary.json")
    return s.get("deadline_ms") if s else None


def main():
    tracks = [t for t in ORDER if (OUT / t / "phase1" / "phase1_summary.json").exists()]
    L = ["# Consolidated Results", "",
         "Auto-generated by `src/gen_results.py` from the committed per-track outputs.",
         "Latencies are environment-specific (Apple M5 MPS / dev GPUs), not reportable in",
         "absolute terms; the cross-method comparisons are the results. CIs are 95% over 5",
         "seeds. The coupling in the coupled regime is a designed modeling assumption; the",
         "uncoupled regime is the control. Pseudo-GT = agreement with the C1 reference.", ""]

    # 1. Data summary
    L += ["## 1. Track / data summary", "",
          "| track | frames | fault % | contention p95x | coupled r | uncoupled r | deadline (ms) |",
          "|---|---|---|---|---|---|---|"]
    for t in tracks:
        p1 = jload(OUT / t / "phase1" / "phase1_summary.json")
        ff = p1.get("trackA_fault_frac", p1.get("fault_frac"))
        c = p1.get("contention", {}).get("p95_shift_ratio")
        reg = p1.get("regimes", {})
        dl = deadline_ms(t)
        L.append(f"| {NAMES.get(t,t)} | {p1.get('n_frames','?')} | "
                 f"{ff*100:.1f}% | {c:.2f}x | {reg.get('coupled',{}).get('pearson_r',float('nan')):.2f} | "
                 f"{reg.get('uncoupled',{}).get('pearson_r',float('nan')):.2f} | "
                 f"{dl:.0f} |" if ff is not None and c and dl else f"| {NAMES.get(t,t)} | ... |")

    # 2. RQ-H
    L += ["", "## 2. RQ-H (headline): joint vs decoupled deadline-miss",
          "Reduction = decoupled minus joint (positive = coupling helps). Control = uncoupled regime.", "",
          "| track | kappa | joint miss | decoupled miss | reduction (95% CI) | sig | uncoupled |",
          "|---|---|---|---|---|---|---|"]
    for t in tracks:
        s = jload(OUT / t / "phase5" / "phase5_rqh.json")
        if not s:
            continue
        su = s["summary"]; d = su["coupled"]; df = d["miss_reduction_decoupled_minus_joint"]
        u = su["uncoupled"]["miss_reduction_decoupled_minus_joint"]
        L.append(f"| {NAMES.get(t,t)} | {d['kappa_mean']:.2f} | {d['joint_miss']['mean']:.3f} | "
                 f"{d['decoupled_miss']['mean']:.3f} | {df['mean']*100:+.2f}pp "
                 f"[{df['lo']*100:.2f},{df['hi']*100:.2f}] | {'YES' if df['significant'] else 'no'} | "
                 f"{u['mean']*100:+.2f}pp |")

    # 3. RQ-A1
    L += ["", "## 3. RQ-A1: persistence (belief) vs memoryless switching, matched accuracy", "",
          "| track | belief switch | memoryless switch | verdict |", "|---|---|---|---|"]
    for t in tracks:
        a = jload(OUT / t / "phase6" / "phase6_results.json")
        if not a:
            continue
        a1 = a["rq_a1"]
        L.append(f"| {NAMES.get(t,t)} | {a1['belief']['mean']:.3f} | {a1['memoryless']['mean']:.3f} | "
                 f"{a1['verdict'].split(':')[0]} |")

    # 4. Learned router
    L += ["", "## 4. Learned routing baseline: joint vs learned MLP (same belief features)",
          "Both use the same features + matched hysteresis; MLP trained on oracle labels (sequence split).", "",
          "| track | joint miss | learned miss | learned-minus-joint (95% CI) | result |",
          "|---|---|---|---|---|"]
    for t in tracks:
        r = jload(OUT / t / "extras" / "learned_router.json")
        if not r:
            continue
        d = r["learned_minus_joint_miss"]
        L.append(f"| {NAMES.get(t,t)} | {r['joint_miss']['mean']:.3f} | {r['learned_miss']['mean']:.3f} | "
                 f"{d['mean']*100:+.2f}pp [{d['lo']*100:.2f},{d['hi']*100:.2f}] | {r['verdict'].split(':')[0]} |")

    # 5. Phase diagram
    L += ["", "## 5. RQ-H phase diagram (coupling-strength x onset-rate sweep)",
          "Significant-positive cells out of 20, and peak joint-vs-decoupled reduction. Coupling=0 row is the control (0).", "",
          "| track | sig+ cells | peak reduction |", "|---|---|---|"]
    for t in tracks:
        d = jload(OUT / t / "extras" / "phase_diagram.json")
        if not d:
            continue
        sig = sum(1 for c in d["cells"] if c["significant"] and c["miss_reduction"] > 0)
        peak = max(c["miss_reduction"] for c in d["cells"])
        L.append(f"| {NAMES.get(t,t)} | {sig}/{len(d['cells'])} | {peak*100:.1f}pp |")

    # 6. Extended frontier
    ext = sorted(glob.glob(str(OUT / "*_extended" / "phase3" / "frontier_table.csv")))
    if ext:
        L += ["", "## 6. Extended dense 9-config frontier"]
        for e in ext:
            tname = Path(e).parts[-3].replace("_extended", "")
            df = pd.read_csv(e)
            L += [f"", f"**{NAMES.get(tname, tname)}** (dense Pareto set):", "",
                  "| config | model | imgsz | accuracy | nom latency (ms) | meets deadline (contended) |",
                  "|---|---|---|---|---|---|"]
            for _, r in df.iterrows():
                L.append(f"| {r['config']} | {r['model']} | {int(r['imgsz'])} | {r['acc_overall']:.2f} | "
                         f"{r['lat_median_nom_ms']:.1f} | {r['meets_deadline_con']} |")

    # 7. Robustness: threshold rule, on-time utility, bootstrap
    rob = {t: jload(OUT / t / "extras" / "robustness.json") for t in tracks}
    rob = {t: r for t, r in rob.items() if r}
    if rob:
        L += ["", "## 7. Robustness: threshold rule, on-time utility, temporal bootstrap",
              "Same draws as RQ-H. Threshold rule = hard union `max(b_c,1[b_f>0.5])`; utility",
              "`U=mean a_t*1[lat<=D]` (late frame scores 0); bootstrap = moving-block (50, B=2000),",
              "pooled across seeds, of the per-frame miss reduction.", "",
              "| track | decoup miss | threshold miss | joint miss | joint-vs-threshold (95% CI) | U joint | U decoup | dU joint-decoup (95% CI) | bootstrap miss-reduction (95% CI) |",
              "|---|---|---|---|---|---|---|---|---|"]
        for t in tracks:
            r = rob.get(t)
            if not r:
                continue
            m = r["miss"]; u = r["utility"]
            jt = r["joint_minus_threshold_miss"]; du = r["utility_joint_minus_decoupled"]
            b = r["bootstrap_miss_reduction"]
            L.append(f"| {NAMES.get(t,t)} | {m['decoupled']['mean']:.3f} | {m['threshold']['mean']:.3f} | "
                     f"{m['joint']['mean']:.3f} | {jt['mean']*100:+.2f}pp [{jt['lo']*100:.2f},{jt['hi']*100:.2f}]"
                     f"{'*' if jt['significant'] else ''} | {u['joint']['mean']:.3f} | {u['decoupled']['mean']:.3f} | "
                     f"{du['mean']*100:+.2f}pp [{du['lo']*100:.2f},{du['hi']*100:.2f}]{'*' if du['significant'] else ''} | "
                     f"{b['mean_pp']:+.2f}pp [{b['lo_pp']:.2f},{b['hi_pp']:.2f}] |")
        # fog sweeps
        fog = rob.get("trackD_fog_6_0")
        if fog and "deadline_sweep" in fog:
            L += ["", "**RADIATE fog deadline-strictness sweep** (multiplier x self-calibrated deadline):", "",
                  "| mult | deadline (ms) | reduction (95% CI) |", "|---|---|---|"]
            for s in fog["deadline_sweep"]:
                L.append(f"| {s['mult']}x | {s['deadline_ms']:.0f} | {s['reduction_pp']:+.2f}pp "
                         f"[{s['lo_pp']:.2f},{s['hi_pp']:.2f}]{'*' if s['significant'] else ''} |")
            L += ["", "**RADIATE fog kappa sweep** (joint-vs-decoupled reduction; kappa=0 collapses to decoupled):", "",
                  "| kappa | reduction (95% CI) |", "|---|---|"]
            for s in fog["kappa_sweep"]:
                L.append(f"| {s['kappa']} | {s['reduction_pp']:+.2f}pp [{s['lo_pp']:.2f},{s['hi_pp']:.2f}]"
                         f"{'*' if s['significant'] else ''} |")

    L += ["", "## Figures (committed under each track's outputs)",
          "- RQ-H per track: `outputs/<track>/phase5/rqh_centerpiece.png`",
          "- RQ-A1 / RQ-A2: `outputs/<track>/phase6/`",
          "- frontier: `outputs/<track>/phase3/frontier.png`",
          "- learned router / phase diagram: `outputs/<track>/extras/`",
          "", "See `REPORT.md` for per-phase verdicts and `WRITEUP.md` for the narrative + limitations.", ""]

    (ROOT / "RESULTS.md").write_text("\n".join(L), encoding="utf-8")
    print(f"wrote RESULTS.md ({len(tracks)} tracks)")


if __name__ == "__main__":
    raise SystemExit(main())
